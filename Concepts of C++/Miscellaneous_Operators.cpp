/*
Miscellaneous Operators : 

1.) [sizeof()]          : returns size of variables.
2.) [cast]              : convert one data_type to another.
3.) [condition ? x : y] : returns value of x if condition is true else y.
4.) [comma(,)]          : causes a sequence of operations to be performed.
5.) [&]                 : returns address of variable.
6.) [*]                 : pointer to a variable.
*/