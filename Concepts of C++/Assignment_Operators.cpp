/*
Assignment Operators : 
Also known as short-hand operators (it basically helps to reduce the token count, other than [=]).

1.) [=]  ---> Assigns value of right to left operand.
1.) [+=] ---> Assigns sum of two to left operand.
1.) [-=] ---> Assigns difference of two left operand.
1.) [*=] ---> Assigns product of two left operand.
1.) [/=] ---> Assigns quotient of two left operand. 
*/